//Quick Sort

//Logic behind Quick Sort:

//Split the array into 1 length long arrays
//Pick a piviotal point, any point!
//Check all the other arrays, and position them to the left(less than the pivit point), position
//the rest on the right(greater than pivit point).

//Programming logic:
//Pick a point, first point in the array.
//Check everything on the RHS of the point(pivot).
//Track and move all numbers lower than pivot point to the close right of the pivot point(example below)
//Move tracked numbers to the LHS.
//Repeat

